document.body.addEventListener('click', function(e) {
  var group, buttons, chart, el;
  if (e.target.nodeName === 'BUTTON' && e.target.parentNode.classList.contains('button-group')) {
    if (e.target.classList.contains('active')) return true;
    group = e.target.parentNode;
    el = group.nextElementSibling;
    buttons = group.querySelectorAll('button');
    chartType = e.target.getAttribute('data-type');

    Array.prototype.forEach.call(buttons, function(el, i){
      el.classList.remove('active');
    });
    e.target.classList.add('active');

    el.setAttribute('data-type', chartType);
    if (chartType == 'langs') {
      renderChart(langData, content.offsetWidth - 60, content.offsetWidth/1.5, 40);
    } else {
      renderChart(placeData, content.offsetWidth - 60, content.offsetWidth/1.5, 40);
    }

  }

  if (e.target.nodeName === 'path' && e.target.classList.contains('slice')) {
    if (e.target.classList.contains('active')) return true;
    openSlice(e.target);
  }

  if (e.target.nodeName === 'text' && e.target.classList.contains('label')) {

    if (e.target.classList.contains('active')) return true;
    openSlice(e.target);
  }
});


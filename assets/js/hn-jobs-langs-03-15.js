function clone(obj) {
    if(obj == null || typeof(obj) != 'object')
        return obj;

    var temp = obj.constructor(); // changed

    for(var key in obj) {
        if(obj.hasOwnProperty(key)) {
            temp[key] = clone(obj[key]);
        }
    }
    return temp;
}

var chart = {};

function renderChart(data, width, height, margin, animate) {
  chart.width = width;
  chart.height = height;
  var speed = animate === false ? 0 : 150;

  var el = document.getElementById('chart');
  if (animate !== false) el.style.opacity = 0;

  setTimeout(function() {
    el.innerHTML = '';

    chart.svg = d3.select("#chart")
      .append("svg")
      .attr("width", width + margin + margin)
      .attr("height", height + margin + margin)
      .append("g")

    chart.svg.append("g")
      .attr("class", "slices");
    chart.svg.append("g")
      .attr("class", "labels");
    chart.svg.append("g")
      .attr("class", "lines");

    chart.radius = Math.min(width, height) / 2;

    chart.pie = d3.layout.pie()
      .value(function(d) {
        return d.value;
      })
      .sort(function(d, x) {
        return d3.ascending(d.name, x.name);
      });

    chart.arcBase = d3.svg.arc()
      .cornerRadius(2);

    chart.arc = d3.svg.arc()
      .outerRadius(chart.radius * 0.8)
      .innerRadius(chart.radius * 0.4)
      .cornerRadius(5);


    chart.outerArc = d3.svg.arc()
      .innerRadius(chart.radius * 0.9)
      .outerRadius(chart.radius * 0.9);

    chart.svg.attr("transform", "translate(" + (chart.width / 2 + margin) + "," + chart.height / 2 + ")");

    chart.color = d3.scale.category20();

    change(data.type, chart, data.dataArray, animate);

    if (animate !== false) el.style.opacity = 1;
  }, speed);

}

function change(type, chart, data, animate) {
  var speed = animate === false ? 0 : 1000;
  /* ------- PIE SLICES -------*/
  var slice = chart.svg.select(".slices")
    .selectAll("path.slice")
    .data(chart.pie(data), chart.key);

  slice.enter()
    .insert("path")
    .style("fill", function(d) { if (d.data.name == 'na') { return '#aaa' } else { return chart.color(d.data.label); }})
    .attr("class", "slice")
    .attr('data-type', type)
    .attr('data-value', function(d) { return d.data.name })
    .each(function(d) {
      this._current = clone(d);
      this._current.startAngle = 0;
      this._current.endAngle = 0;
      this._current.innerRadius = chart.radius * 0.4;
      this._current.outerRadius = chart.radius * 0.8;
    });

  slice
    .transition()
    .duration(speed)
    .attrTween("d", function(d) {
      if (chart.active === d.data.name) {
        d.outerRadius = chart.radius * 0.9;
      } else {
        d.outerRadius = chart.radius * 0.8;
      }
      var interpolate = d3.interpolate(this._current, d);
      this._current = interpolate(0);
      return function(t) {
        return chart.arcBase(interpolate(t));
      };
    })

  slice.exit()
    .remove();

  /* ------- TEXT LABELS -------*/

  var text = chart.svg.select(".labels").selectAll("text")
    .data(chart.pie(data), chart.key);

  text.enter()
    .append("text")
    .attr('class', 'label')
    .attr('data-type', type)
    .attr('data-value', function(d) { return d.data.name })
    .attr("dy", ".35em")
    .text(function(d) {
      return d.data.label;
    })
    .each(function(d) {
      this._current = clone(d);
      this._current.startAngle = 0;
      this._current.endAngle = 0;
    });

  function midAngle(d){
    return d.startAngle + (d.endAngle - d.startAngle)/2;
  }

  text.transition()
    .attrTween("transform", function(d) {
      this._current = d;
      var interpolate = d3.interpolate(this._current, d);
      this._current = interpolate(0);
      return function(t) {
        var d2 = interpolate(t);
        var pos = chart.outerArc.centroid(d2);
        pos[0] = chart.radius * (midAngle(d2) < Math.PI ? 1 : -1);

        return "translate("+ pos +")";
      };
    })
    .styleTween("text-anchor", function(d){
      var interpolate = d3.interpolate(this._current, d);
      this._current = interpolate(0);
      return function(t) {
        var d2 = interpolate(t);
        return midAngle(d2) < Math.PI ? "start":"end";
      };
    });

  if (animate !== false) {
    chart.svg.select(".labels").node().style.opacity = 0;
    setTimeout(function() {
      chart.svg.select(".labels").node().style.opacity = 1;
    }, speed / 100 * 70);
  }

  text.exit()
    .remove();

  /* ------- SLICE TO TEXT POLYLINES -------*/

  var polyline = chart.svg.select(".lines").selectAll("polyline")
    .data(chart.pie(data), chart.key)

  polyline.enter()
    .append("polyline")
    .each(function(d) {
      this._current = clone(d);
      this._current.startAngle = 0;
      this._current.endAngle = 0;
    });

  polyline.transition().duration(0)
    .attrTween("points", function(d){
      var interpolate = d3.interpolate(this._current, d);
      this._current = interpolate(0);
      return function(t) {
        var d2 = interpolate(t);
        var pos = chart.outerArc.centroid(d2);
        pos[0] = chart.radius * 0.95 * (midAngle(d2) < Math.PI ? 1 : -1);
        return [chart.arc.centroid(d2), chart.outerArc.centroid(d2), pos];
      };
    });

  if (animate !== false) {
    chart.svg.select(".lines").node().style.opacity = 0;
    setTimeout(function() {
      chart.svg.select(".lines").node().style.opacity = 1;
    }, speed / 100 * 70);
  }

  polyline.exit()
    .remove();
};

function renderLangJobs(val) {
  var items = data.filter(function(item) {
    if (val === 'na') {
      return item.langs.length == 0;
    } else {
      return (item.langs.indexOf(val) !== -1);
    }
  });
  renderJobItems(items);
}

function renderRemoteJobs(val) {
  var items = data.filter(function(item) {
    if (val === 'remote') return item.remote;
    if (val === 'onsite') return ! item.remote;
  });
  renderJobItems(items);
}

function renderJobItems(items) {
  var el = document.getElementById('jobs');
  el.innerHTML = '';
  setTimeout(function() {
    items.forEach(function(item) {
      setTimeout(function() {
        var html = '<li>';
        html += '<div class="title"><a href="https://news.ycombinator.com/item?id=' + item.id + '">' + item.line + '</a></div>';
        html += '<div class="job-meta">';
        html += 'by <a href="https://news.ycombinator.com/user?id=' + item.by + '">' + item.by + '</a>';
        html += '<span class="badges">'
        item.langs.forEach(function(lang) {
          html += '<span class="badge ' + lang + '" style="background:' +  chart.color(langNames[lang]) + '">' + langNames[lang] + '</span>';
        });
        html += '</span></div>';
        html += '</li>';
        el.innerHTML += html;
      }, 0);
    });
  }, 300);

}

function openSlice(el) {
  var type = el.getAttribute('data-type');
  var value = el.getAttribute('data-value');
  var slice = chart.svg.select(".slices")
    .selectAll("path.slice")
    .attr('class', 'slice');
  el.classList.add('active');

  chart.active = value;

  chart.svg.selectAll('.slice')
    .transition()
    .duration(300)
    .attrTween("d", function(d) {
      if (chart.active === d.data.name) {
        d.outerRadius = chart.radius * 0.9;
      } else {
        d.outerRadius = chart.radius * 0.8;
      }
      var interpolate = d3.interpolate(this._current, d);
      return function(t) {
        return chart.arcBase(interpolate(t));
      };
    })

  if (type == 'langs') {
    renderLangJobs(value);
  } else {
    renderRemoteJobs(value);
  }
}


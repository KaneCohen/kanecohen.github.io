function prepareLangData(data) {
  var prepared = {
    type: 'langs',
    total: data.length,
    withlangs: 0,
    data: {
      na: {
        name: 'na',
        label: 'N/A',
        value: 0
      }
    },
    dataArray: [],
  };
  data.forEach(function(item) {
    if (item.langs.length > 0) {
      item.langs.forEach(function(lang) {
        if (prepared.data[lang]) {
          prepared.data[lang].value++;
        } else {
          prepared.data[lang] = {
            name: lang,
            label: langNames[lang],
            value: 1
          }
        }
      });
    } else {
      prepared.data.na.value++;
    }

  });

  for (k in prepared.data) {
    if (prepared.data.hasOwnProperty(k)) {
      prepared.dataArray.push(prepared.data[k]);
    }
  }
  return prepared;
}

